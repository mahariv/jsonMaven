package fr.iut.paris8.jsonMaven;

public class Warrior {
	
	private String name;
	
	private String type;
	
	private int life;
	
	private String armorName;
	
	private int armorResist;
	
	private String waponName;

	private int waponDamage;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public int getLife() {
		return life;
	}

	public void setLife(int life) {
		this.life = life;
	}

	public String getArmorName() {
		return armorName;
	}

	public void setArmorName(String armorName) {
		this.armorName = armorName;
	}

	public int getArmorResist() {
		return armorResist;
	}

	public void setArmorResist(int armorResist) {
		this.armorResist = armorResist;
	}

	public String getWaponName() {
		return waponName;
	}

	public void setWaponName(String waponName) {
		this.waponName = waponName;
	}

	public int getWaponDamage() {
		return waponDamage;
	}

	public void setWaponDamage(int waponDamage) {
		this.waponDamage = waponDamage;
	}

}
